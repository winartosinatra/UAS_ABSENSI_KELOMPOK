public class sisw {
    String ids,nis,namal;
    public sisw(){
        super();

        this.ids = ids;
        this.nis = nis;
        this.namal = namal;

    }

    public String getIds() {
        return ids;
    }

    public String getNamal() {
        return namal;
    }

    public String getNis() {
        return nis;
    }

    public void setIds(String ids) {
        this.ids = ids;
    }

    public void setNamal(String namal) {
        this.namal = namal;
    }

    public void setNis(String nis) {
        this.nis = nis;
    }
}
